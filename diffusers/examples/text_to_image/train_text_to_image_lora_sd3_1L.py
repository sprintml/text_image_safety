def log_validation(
    pipeline,
    args,
    accelerator,
    epoch,
    ocr_model,
    clip_model,
    prompts,
    texts,
    is_final_validation=False,
):
    pipeline = pipeline.to(accelerator.device)
    pipeline.set_progress_bar_config(disable=True)

    # run inference
    generator = torch.Generator(device=accelerator.device).manual_seed(args.seed) if args.seed else None
    if torch.backends.mps.is_available():
        autocast_ctx = nullcontext()
    else:
        autocast_ctx = torch.autocast(accelerator.device.type)

    images = []
    for i in tqdm(range(0, len(prompts), args.val_batch_size), desc="Running batch inference...", unit="batch"):
        batch_prompts = prompts[i : i + args.val_batch_size]

        with autocast_ctx:
            batch_images = pipeline(
                prompt=batch_prompts,
                generator=generator,
                num_inference_steps=args.num_inference_steps,
            ).images

            images.extend(batch_images)
    logger.info("Finished running validation")
    metrics = calculate_metrics(
        ocr_model, images, prompts, texts, accelerator.device, args.val_batch_size, clip_model, epoch
    )
    ocr_texts = metrics["OCR_texts"]

    for tracker in accelerator.trackers:
        phase_name = "test" if is_final_validation else "validation"
        if tracker.name == "tensorboard":
            np_images = np.stack([np.asarray(img) for img in images])
            tracker.writer.add_images(phase_name, np_images, epoch, dataformats="NHWC")
        if tracker.name == "wandb":
            images_to_log = images[:10]
            images_to_log.extend(images[400:410])
            prompts_to_log = prompts[:10]
            prompts_to_log.extend(prompts[400:410])
            ocr_texts_to_log = ocr_texts[:10]
            ocr_texts_to_log.extend(ocr_texts[400:410])
            tracker.log(
                {
                    f"{phase_name}/images": [
                        wandb.Image(image, caption=f"prompt: {prompts_to_log[i]} \nOCR text: {ocr_texts_to_log[i]}")
                        for i, image in enumerate(images_to_log)
                    ]
                }
            )
            tracker.log({f"{phase_name}/{m}": np.mean(v) for m, v in metrics.items() if m != "OCR_texts"})
    np.save(os.path.join(args.output_dir, f"images_{epoch}.npy"), np.stack([np.asarray(img) for img in images]))
    return images


def import_model_class_from_model_name_or_path(
    pretrained_model_name_or_path: str, revision: str, subfolder: str = "text_encoder"
):
    text_encoder_config = PretrainedConfig.from_pretrained(
        pretrained_model_name_or_path, subfolder=subfolder, revision=revision
    )
    model_class = text_encoder_config.architectures[0]

    if model_class == "CLIPTextModel":
        from transformers import CLIPTextModel

        return CLIPTextModel
    elif model_class == "CLIPTextModelWithProjection":
        from transformers import CLIPTextModelWithProjection

        return CLIPTextModelWithProjection
    elif model_class == "T5EncoderModel":
        from transformers import T5EncoderModel

        return T5EncoderModel
    else:
        raise ValueError(f"{model_class} is not supported.")


