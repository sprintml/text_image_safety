def calculate_metrics(ocr_model, images, prompts, texts, device, batch_size, clip_model, epoch):
    # calculate metrics per sample
    # OCR Acc/Prec/Rec
    logger.info("Calculating OCR metrics")
    ocr_texts = [get_text_easyocr(ocr_model, np.array(images[i]).astype(np.uint8)).lower() for i in range(len(images))]
    ocr_pr, ocr_rec, ocr_acc = ocr_metrics(ocr_texts, texts)
    # Levenshtein distance
    leve = get_levenshtein_distances(ocr_texts, texts)
    # clip text
    logger.info("Calculating CLIP metrics")
    image_feats = extract_all_images(images, clip_model, device, batch_size=batch_size)
    np.save(os.path.join(args.output_dir, f"clip_feats_{epoch}.npy"), image_feats)
    _, prompt_sim, _ = get_clip_score(
        clip_model,
        image_feats,
        prompts,
        device,
    )
    logger.info("Finished calculating metrics")
    return {
        "OCR_Prec": ocr_pr,
        "OCR_Rec": ocr_rec,
        "OCR_Acc": ocr_acc,
        "Levenshtein": leve,
        "OCR_texts": ocr_texts,
        "OCR_F1": (2 * np.mean(ocr_pr) * np.mean(ocr_rec)) / (np.mean(ocr_pr) + np.mean(ocr_rec) + 1e-6),
        "CLIP-T": prompt_sim,
    }


